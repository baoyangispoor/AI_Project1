public class Point {
    int x;//x index
    int y;//y index
    int idx;// index in the list
    String cityName;//name of city

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getIdx() {
        return idx;
    }

    public String getCityName() {
        return cityName;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public void setIdx(int idx) {
        this.idx = idx;
    }

    public void setCityName(String cityName) {
        this.cityName = cityName;
    }
}
